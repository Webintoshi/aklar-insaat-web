async function getWhatsAppConfig() {
  const supabase = await createClient()
  const { data } = await supabase
    .from('whatsapp_widget_config')
    .select('*')
    .order('created_at', { ascending: false })
    .limit(1)
    .maybeSingle()
  
  return data
}

